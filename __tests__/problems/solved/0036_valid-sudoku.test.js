import { describe, expect, test } from 'bun:test';
import { isValidSudoku } from '../../../src/problems/solved/0036_valid-sudoku.js';

describe('isValidSudoku', () => {
        test('returns true for valid sudokus', () => {
                expect(
                        isValidSudoku([
                                ['5', '3', '.', '.', '7', '.', '.', '.', '.'],
                                ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
                                ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
                                ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
                                ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
                                ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
                                ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
                                ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
                                ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
                        ]),
                ).toBe(true);
        });

        test('returns false for invalid sudokus', () => {
                expect(
                        isValidSudoku([
                                ['8', '3', '.', '.', '7', '.', '.', '.', '.'],
                                ['6', '.', '.', '1', '9', '5', '.', '.', '.'],
                                ['.', '9', '8', '.', '.', '.', '.', '6', '.'],
                                ['8', '.', '.', '.', '6', '.', '.', '.', '3'],
                                ['4', '.', '.', '8', '.', '3', '.', '.', '1'],
                                ['7', '.', '.', '.', '2', '.', '.', '.', '6'],
                                ['.', '6', '.', '.', '.', '.', '2', '8', '.'],
                                ['.', '.', '.', '4', '1', '9', '.', '.', '5'],
                                ['.', '.', '.', '.', '8', '.', '.', '7', '9'],
                        ]),
                ).toBe(false);
        });
});
